package gao.java;

public class Teacher {
	private int Tea_ID;
	private String Tea_name;
	private String Tea_psd;	
	public int getTea_ID() {
		return Tea_ID;
	}	
	public void setTea_ID(int tea_ID) {
		Tea_ID = tea_ID;
	}	
	public String getTea_name() {
		return Tea_name;
	}
	public void setTea_name(String tea_name) {
		Tea_name = tea_name;
	}
	public String getTea_psd() {
		return Tea_psd;
	}
	public void setTea_psd(String tea_psd) {
		Tea_psd = tea_psd;
	}
	
}
